struct threadCtx
{
    int     fd;
    char   *buf;
    size_t  bufSize;
};

static size_t ReadSize;
static size_t WriteSize;

//--------------------------------------------------------------------------------------------------
// Test functions
//--------------------------------------------------------------------------------------------------

static void *FifoReadCloseMain(void *context)
{
    struct threadCtx *tCtx = (struct threadCtx *)context;
    ssize_t ret;

    LE_TEST_INFO("Started");

    memset(tCtx->buf, 0, tCtx->bufSize);
    ret = le_fd_Read(tCtx->fd, tCtx->buf, tCtx->bufSize);

    if ((-1 == ret) && (errno != EAGAIN))
    {
        LE_TEST_INFO("error: %s", strerror(errno));
    }

    le_fd_Close(tCtx->fd);

    LE_TEST_INFO("Read end closed, totalRead %zu", ret);

    return NULL;
}

static void *FifoWriteReadCloseMain(void *context)
{
    struct threadCtx *tCtx = (struct threadCtx *)context;
    ssize_t ret;

    LE_TEST_INFO("Started with bufSize %zu", tCtx->bufSize);

    ret = le_fd_Write(tCtx->fd, tCtx->buf, tCtx->bufSize);
    LE_TEST_OK((-1 == ret) && (EPIPE == errno), "error %s received ", strerror(errno));
    le_fd_Close(tCtx->fd);

    LE_TEST_INFO("Write ended");

    return NULL;
}

static void *FifoReadMain(void *context)
{
    struct threadCtx *tCtx = (struct threadCtx *)context;
    ssize_t ret, totalRead = 0;

    LE_TEST_INFO("Started");

    ReadCrc = LE_CRC_START_CRC32;
    do
    {
        memset(tCtx->buf, 0, tCtx->bufSize);
        ret = le_fd_Read(tCtx->fd, tCtx->buf, tCtx->bufSize);
        if (ret == -1 && errno != EAGAIN)
        {
            break;
        }
        else if (ret > 0)
        {
            ReadCrc = le_crc_Crc32((uint8_t*)tCtx->buf, ret, ReadCrc);
            totalRead += ret;
        }
    }
    while (ret != 0);

    LE_TEST_INFO("Read ended, totalRead %zd", totalRead);
    ReadSize = totalRead;
    return NULL;
}

static void *FifoWriteMain(void *context)
{
    struct threadCtx *tCtx = (struct threadCtx *)context;
    ssize_t ret;

    LE_TEST_INFO("Started with bufSize %zu", tCtx->bufSize);

    ret = le_fd_Write(tCtx->fd, tCtx->buf, tCtx->bufSize);
    if ((ssize_t) tCtx->bufSize == ret)
    {
        WriteSize = ret;
    }

    LE_TEST_INFO("Write ended");

    return NULL;
}

